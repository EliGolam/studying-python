"""
Practice with string methods
"""

character = 'briTnEY'

print(f"String capitalize: {character.capitalize()}")
print(f"String endswith y? {character.endswith('y')}")
print(f"String find 'it': {character.find('it')}")
print(f"String find 'abri': {character.find('abri')}")
print(f"String isalnum: {character.isalnum()}")
print(f"String isalpha: {character.isalpha()}")
print(f"String isnumeric: {character.isnumeric()}")

